        <h4>What can SPF do?</h4>
            <p>With SPF you can build any website, for example: webshops or blogs.
            But also just a 1 page website and much more. SPF can be used for small home projects to large systems for corporations</p>
            <p>Summed up, you can do everything with SPF, because it's not limited to anything</p>
        <h4>How to use SPF?</h4>
        	<ol>
            <li>Go to CMS</li>
            <li>(optional) read documentation</li>
            <li>Install desired Modules</li>
            <li>Create Page (you can use the page_creator module for this)</li>
            <li>(optional) load UniGUI module in your page and choose a style</li>
            <li>(optional) Use the authentication module to protect your page with an authentication prompt</li>
            </ol>
            <p>I recommend reading the documentation, for information about every plugin and how to use SPF</p>
        <h4>How does SPF work?</h4>
        	<ol>
            <li>~/SPF/index.php is loaded</li>
            <li>pagename is acquired from URL</li>
            <li>Info for "Pagename"  is downloaded from DB</li>
            <li>Load Settings</li>
            <li>Load Modules</li>
            <li>Load page</li>
            </ol>
